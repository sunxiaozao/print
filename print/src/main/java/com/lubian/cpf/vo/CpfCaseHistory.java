    public void fixup(){
        if ( this.cpfPatient != null ) {
            for ( int i = 0, n = cpfPatient.size(); i < n; i++ ) { 
              CpfPatient oneCpfPatient=(CpfPatient) cpfPatient.get(i);
              oneCpfPatient.setId(this.patientId);
              oneCpfPatient.fixup();
          }
      }
        if ( this.sysHospital != null ) {
            for ( int i = 0, n = sysHospital.size(); i < n; i++ ) { 
              SysHospital oneSysHospital=(SysHospital) sysHospital.get(i);
              oneSysHospital.setId(this.hospitalId);
              oneSysHospital.fixup();
          }
      }
    }

