    public void fixup(){
        if ( this.role != null ) {
            for ( int i = 0, n = role.size(); i < n; i++ ) { 
              SysRole oneSysRole=(SysRole) role.get(i);
              oneSysRole.setRoleId(this.roleId);
              oneSysRole.fixup();
          }
      }
        if ( this.userOrg != null ) {
            for ( int i = 0, n = userOrg.size(); i < n; i++ ) { 
              SysOrg oneSysOrg=(SysOrg) userOrg.get(i);
              oneSysOrg.setOrgId(this.orgId);
              oneSysOrg.fixup();
          }
      }
    }

